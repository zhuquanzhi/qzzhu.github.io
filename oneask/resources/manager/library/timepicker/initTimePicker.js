$(document).ready(function(){
	$('.dateWithDay').datetimepicker({
		format:'yyyy-mm-dd',
		startView:2,
		minView:2,
		autoclose:true
	});
	$('.dateWithYear').datetimepicker({
		format:'yyyy',
		startView:4,
		maxView:1,
		minView:4,
		autoclose:true
	});
	$('.dateWithYearMonth').datetimepicker({
		 format:'yyyy-mm',
	     startView:3,
	     minView:3,
	     autoclose:true
	});
	//开始时间
	$("#datetimeStartMonth").datetimepicker({
		format:'yyyy-mm',
	    startView:3,
	    minView:3,
	    autoclose:true
    }).on("click",function(){
    	//设置可选的最迟时间为结束时间
        $("#datetimeStartMonth").datetimepicker("setEndDate",$("#datetimeEndMonth").val());
    });
	//结束时间
    $("#datetimeEndMonth").datetimepicker({
    	format:'yyyy-mm',
	    startView:3,
	    minView:3,
	    autoclose:true
    }).on("click",function(){
    	//设置可选的最早时间为开始时间
        $("#datetimeEndMonth").datetimepicker("setStartDate",$("#datetimeStartMonth").val());
    });
});